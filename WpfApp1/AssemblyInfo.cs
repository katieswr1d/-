using System.Windows;

[assembly: ThemeInfo(
    ResourceDictionaryLocation.None, //where theme specific resource dictionaries are located
    //(used if a resource is not found in the page,
    // or application resource dictionaries)
    ResourceDictionaryLocation.SourceAssembly //where the generic resource dictionary is located
    //(used if a resource is not found in the page,
    // app, or any theme specific resource dictionaries)
)]

/*
 *  класс с атрибутом ThemeInfo служит для настройки управления ресурсами и темами оформления в WPF-приложении.
 *  Он позволяет разработчику определить, где искать ресурсы в зависимости от контекста, что упрощает работу с интерфейсом и улучшает пользовательский опыт.
*/